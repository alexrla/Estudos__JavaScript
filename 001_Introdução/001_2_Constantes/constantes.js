// Constantes

/*
    - Introduzida a partir da versão ES6 do JavaScript;

    - Usamos a palavra reservada const para declarar uma constante;

    - Uma vez inicializada, uma constante não pode ter seu valor alterado;

    - Não podemos declarar uma constante sem inicializá-la;

    - Alguns navegadores não suportam o uso de const;
*/

// Declarando e inicializando uma constante
const number = 5;

// Erro
number = 10;