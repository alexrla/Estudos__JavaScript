// console.log

/*
    - Todos os navegadores modernos possuem um console para depuração;

    - O método console.log() é usado para exibir informações nesses consoles;
*/

let number = 2;
console.log(number); // 2

/*
    - Sintaxe: console.log(valor/variável);
*/

// Exibindo uma string
console.log("JavaScript!");

// Exibindo valores armazenados em variáveis
const greeting = "Olá";
const name = "Leandro";

console.log(`${greeting} ${name}!`);
